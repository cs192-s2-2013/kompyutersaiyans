package com.uportal.domain;

public class Feedback {
	String username;
	String feed;
	
	public String getUsername() {  
		return username;  
	}  
	 
	public void setUsername(String username) {  
		this.username = username;  
	}  
	 
	public String getFeed() {  
		return feed;  
	}  
	 
	public void setFeed(String feed) {  
		this.feed = feed;  
	}  
}
